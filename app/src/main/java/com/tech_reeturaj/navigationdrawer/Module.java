package com.tech_reeturaj.navigationdrawer;

public class Module
{
    private int image;
    private String name;

    public Module() {
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
